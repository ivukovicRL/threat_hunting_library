import tldextract
from rl_threat_hunting.utils import is_python2_executing


if is_python2_executing():
    uri_parser = tldextract.TLDExtract(cache_file=False)
else:
    uri_parser = tldextract.TLDExtract(cache_dir=False)

# we want to skip single number IPs like 0.0.0.0 and anything else that's short
# they're usually FP extractions from file, not actual interesting URIs
#
MIN_INTERESTING_URI_LENGTH = 8


def is_interesting_uri(value):
    if len(value) < MIN_INTERESTING_URI_LENGTH:
        return False

    if value in WHITELISTED_URIS:
        return False

    extracted_uri = uri_parser(value)
    if extracted_uri.domain and extracted_uri.suffix:
        partial_uri = u'{}.{}'.format(extracted_uri.domain, extracted_uri.suffix)
        if partial_uri in WHITELISTED_URIS:
            return False

    return True
